package com.dinginfo.seamq.entity;

import java.io.Serializable;
import java.util.Date;

public class MQDomain implements Serializable{
	public static final long DEFAULT_DOMAIN_ID = 1L;

	private long id;

	private String name;
	
	private Date createdTime;

	public MQDomain(){}
	
	public MQDomain(long id){
		this.id = id;
	}
	
	public MQDomain(long id,String name){
		this.id = id;
		this.name = name;
	}
	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

}
